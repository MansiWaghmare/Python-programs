# to input length, breadth of rectangle and calculate its area
length=float(input("Enter the length of rectangle = "))
breadth=float(input("Enter the breadth of rectangle = "))
units=str(input("units = "))
print("Length = ",length,units)
print("Breadth = ",breadth,units)
area=length*breadth
print("Area = length * breadth")
print("Area = ",length,units,"*",breadth,units)
print("Area of rectangle = ",area,"sq",units)